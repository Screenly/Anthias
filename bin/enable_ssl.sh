#!/bin/bash -e

# Enable HTTPS on Anthias by pointing uvicorn at a TLS certificate and
# private key. Two modes:
#
#   bin/enable_ssl.sh                            # generate a self-signed cert
#   bin/enable_ssl.sh --cert C.pem --key K.pem   # install user-supplied cert
#
# The cert + key are copied to ~/.anthias/ssl/ and a compose override is
# written so anthias-server is restarted on port 443 with SSL_CERTFILE /
# SSL_KEYFILE pointing at the mounted files.
#
# Disable later with: bin/disable_ssl.sh
#
# Note: bin/add_certificate.sh is the *outbound* counterpart — it
# installs a CA into the trust stores of anthias-server and
# anthias-viewer so Anthias can verify private HTTPS asset hosts. The
# two scripts are independent.

USER_CERT=""
USER_KEY=""

while [[ $# -gt 0 ]]; do
    case "$1" in
        --cert)
            USER_CERT="$2"
            shift 2
            ;;
        --key)
            USER_KEY="$2"
            shift 2
            ;;
        -h|--help)
            sed -n '3,18p' "$0"
            exit 0
            ;;
        *)
            echo "Unknown argument: $1" >&2
            exit 2
            ;;
    esac
done

if [[ -n "$USER_CERT" && -z "$USER_KEY" ]] \
   || [[ -z "$USER_CERT" && -n "$USER_KEY" ]]; then
    echo "--cert and --key must be supplied together." >&2
    exit 2
fi

SSL_DIR="${HOME}/.anthias/ssl"
COMPOSE_DIR="${HOME}/anthias"
OVERRIDE_FILE="${COMPOSE_DIR}/docker-compose.ssl.override.yml"

mkdir -p "$SSL_DIR"
chmod 700 "$SSL_DIR"

if [[ -n "$USER_CERT" ]]; then
    if [[ ! -f "$USER_CERT" ]]; then
        echo "Cert not found: $USER_CERT" >&2
        exit 1
    fi
    if [[ ! -f "$USER_KEY" ]]; then
        echo "Key not found: $USER_KEY" >&2
        exit 1
    fi
    echo "Installing user-supplied certificate from $USER_CERT..."
    cp "$USER_CERT" "$SSL_DIR/cert.pem"
    cp "$USER_KEY" "$SSL_DIR/key.pem"
    chmod 600 "$SSL_DIR/key.pem"
elif [[ ! -f "$SSL_DIR/cert.pem" || ! -f "$SSL_DIR/key.pem" ]]; then
    echo "Generating self-signed SSL certificate at $SSL_DIR..."
    openssl req -x509 -nodes -newkey rsa:2048 \
        -keyout "$SSL_DIR/key.pem" \
        -out "$SSL_DIR/cert.pem" \
        -days 3650 \
        -subj "/CN=anthias.local"
    chmod 600 "$SSL_DIR/key.pem"
else
    echo "Reusing existing certificate at $SSL_DIR."
fi

# Compose v2.20+ `!override` replaces the base `ports:` list (which
# binds host port 80) with a 443 mapping; merging would otherwise
# leave both 80 and 443 open.
cat > "$OVERRIDE_FILE" <<EOF
# Generated by bin/enable_ssl.sh — delete this file (and re-run
# \`docker compose up -d\`) or run bin/disable_ssl.sh to disable SSL.
services:
  anthias-server:
    ports: !override
      - 443:8080
    environment:
      - SSL_CERTFILE=/data/.anthias/ssl/cert.pem
      - SSL_KEYFILE=/data/.anthias/ssl/key.pem
    volumes:
      - ${HOME}/.anthias/ssl:/data/.anthias/ssl:ro
EOF

echo "Wrote compose override: $OVERRIDE_FILE"
echo "Restarting anthias-server with TLS enabled..."

sudo -E docker compose \
    -f "$COMPOSE_DIR/docker-compose.yml" \
    -f "$OVERRIDE_FILE" \
    up -d --force-recreate anthias-server

echo
echo "SSL enabled. Anthias is now reachable at https://<your IP>"
if [[ -z "$USER_CERT" ]]; then
    echo "(self-signed; browsers will warn on first visit)."
fi
echo
echo "If you have a firewall, open TCP/443 — e.g. \`sudo ufw allow 443/tcp\`."
